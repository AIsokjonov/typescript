function switchFunction(a) {
    switch (a) {
        case 1: {
            var var1_1 = "test";
            console.log(var1_1);
        }
        case 2: {
            var var2_1 = "test2";
            console.log(var2_1);
            break;
        }
        case 3: {
            var var3_1 = "test3";
            console.log(var3_1);
            break;
        }
        default: {
            console.log("default");
        }
    }
}
switchFunction(1);
